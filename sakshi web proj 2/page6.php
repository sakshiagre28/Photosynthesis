        <p class="para">Light reactions need light to produce organic energy molecules (ATP and NADPH). They are initiated by colored pigments, mainly green colored chlorophylls.

        Dark reactions make use of these organic energy molecules (ATP and NADPH). This reaction cycle is also called Calvin Benison Cycle, and it occurs in the stroma. ATP provides the energy while NADPH provides the electrons required to fix the CO<sub>2</sub> (carbon dioxide) into carbohydrates.This means Dark reactions will fail to continue if the plants are deprived of light for too long since they use the output of the initial light-dependent reactions.</p>
